#!/usr/bin/env node
// Reads upload-results.json → generates ipfs-badges.ts for NFTMarketplace.tsx
import fs from 'fs'

const RESULTS = './badges-output/upload-results.json'
const GW = 'https://gateway.pinata.cloud/ipfs'

if (!fs.existsSync(RESULTS)) {
    console.error('❌ Run upload-badges.js first!')
    process.exit(1)
}

const { badges, rarityBreakdown, uploadedAt } = JSON.parse(fs.readFileSync(RESULTS, 'utf8'))

console.log(`📊 Loaded ${badges.length} badges from ${uploadedAt}\n`)
console.log('Rarity breakdown:')
Object.entries(rarityBreakdown).forEach(([r,n]) => console.log(`  ${r.padEnd(10)} ${n}`))

// Build TS export
const ipfsBadges = badges.map(b => ({
    tokenId:     b.seed,
    name:        `B2S Badge #${b.seed}`,
    trait:       b.trait,
    style:       b.style,
    rarity:      b.rarity,
    imageUrl:    `${GW}/${b.imageCID}`,
    metadataUrl: `${GW}/${b.metaCID}`,
}))

const ts = `// Auto-generated by update-marketplace.js — ${uploadedAt}
// Paste this into src/components/NFTMarketplace.tsx

export const IPFS_BADGES = ${JSON.stringify(ipfsBadges, null, 2)}
`

fs.writeFileSync('./badges-output/ipfs-badges.ts', ts)
console.log('\n✅ Generated: badges-output/ipfs-badges.ts')
console.log('   Paste IPFS_BADGES into NFTMarketplace.tsx')

// Show sample
console.log('\n🔗 Sample CIDs:')
badges.slice(0,3).forEach(b => {
    console.log(`  #${b.seed} [${b.rarity}] ${b.trait}`)
    console.log(`    ${GW}/${b.imageCID}`)
})